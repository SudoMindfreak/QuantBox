import dotenv from 'dotenv';
import { ClobClient } from '@polymarket/clob-client';
import { MarketService } from './services/MarketService';
import { MarketResolver } from './services/MarketResolver';
import { OrderbookStream } from './engine/stream';
import { VirtualWallet } from './engine/wallet';
import { OrderBookMessage } from './types/polymarket';

// Load environment variables
dotenv.config();

const POLYMARKET_HOST = 'https://clob.polymarket.com';
const POLYGON_CHAIN_ID = 137;

async function main() {
    console.log('\nüîÆ QuantBox - Virtual Trading Engine Demo\n');
    console.log('üí° Phase 2: URL-Based Market Discovery\n');

    // ========================================
    // 1. Initialize Read-Only CLOB Client
    // ========================================
    console.log('‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ');
    console.log('üìã Step 1: Initialize Read-Only CLOB Client');
    console.log('‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ\n');

    const clobClient = new ClobClient(
        POLYMARKET_HOST,
        POLYGON_CHAIN_ID,
        undefined,
        { key: '', secret: '', passphrase: '' }
    );

    console.log('‚úÖ Read-only client initialized\n');

    // ========================================
    // 2. Resolve Market from URL/Slug
    // ========================================
    console.log('‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ');
    console.log('üìã Step 2: Resolve Market from URL');
    console.log('‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ\n');

    // Get market input from environment or CLI args
    const marketInput = process.env.MARKET_URL ||
        process.argv[2] ||
        'bitcoin-up-or-down-february-4-11am-et';

    console.log(`üéØ Input: ${marketInput}\n`);

    const resolver = new MarketResolver(clobClient);
    const market = await resolver.resolve(marketInput);

    console.log(`\nüìä Market: ${market.question}`);
    console.log(`   Condition ID: ${market.condition_id.substring(0, 12)}...`);
    console.log(`   End Date: ${market.end_date_iso}`);
    console.log(`   Active: ${market.active ? '‚úÖ' : '‚ùå'}`);

    // ========================================
    // 3. Fetch Full Market Metadata from CLOB
    // ========================================
    console.log('\n‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ');
    console.log('üìã Step 3: Fetch Market Metadata from CLOB');
    console.log('‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ\n');

    const marketService = new MarketService(clobClient);
    const fullMarket = await marketService.getMarketByConditionId(market.condition_id);
    const { yes: yesTokenId, no: noTokenId } = marketService.extractTokenIds(fullMarket);

    console.log(`   YES Token: ${yesTokenId.substring(0, 12)}...`);
    console.log(`   NO Token: ${noTokenId.substring(0, 12)}...\n`);

    // ========================================
    // 4. Initialize Virtual Wallet
    // ========================================
    console.log('‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ');
    console.log('üìã Step 4: Initialize Virtual Wallet');
    console.log('‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ\n');

    const initialBalance = parseFloat(process.env.INITIAL_VIRTUAL_BALANCE || '10000');
    const wallet = new VirtualWallet(initialBalance);

    // =======================================
    // 5. Connect to WebSocket Stream
    // ========================================
    console.log('\n‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ');
    console.log('üìã Step 5: Connect to Public Orderbook WebSocket');
    console.log('‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ\n');

    const stream = new OrderbookStream();
    await stream.connect();

    // Subscribe to both YES and NO tokens
    stream.subscribe([yesTokenId, noTokenId]);

    // ========================================
    // 6. Monitor Orderbook Updates
    // ========================================
    console.log('\n‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ');
    console.log('üìã Step 6: Monitor Live Orderbook Data');
    console.log('‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ\n');

    let orderbookData: { [key: string]: OrderBookMessage } = {};
    let hasExecutedTestOrder = false;

    stream.on('orderbook', (message: OrderBookMessage) => {
        const tokenId = message.asset_id;
        const outcome = tokenId === yesTokenId ? 'YES' : 'NO';

        // Store latest orderbook
        orderbookData[tokenId] = message;

        // Display orderbook
        console.log(`\nüìä ${outcome} Token Orderbook Update (${new Date(parseInt(message.timestamp)).toLocaleTimeString()})`);
        console.log('   ‚îå‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îê');
        console.log('   ‚îÇ         ASKS (Selling)          ‚îÇ');
        console.log('   ‚îú‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚î¨‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚î§');

        const asks = message.asks.slice(0, 3);
        asks.reverse().forEach(ask => {
            console.log(`   ‚îÇ $${parseFloat(ask.price).toFixed(4)}  ‚îÇ  ${parseFloat(ask.size).toFixed(2)} shares  ‚îÇ`);
        });

        const bestAsk = parseFloat(message.asks[0]?.price || '0');
        const bestBid = parseFloat(message.bids[0]?.price || '0');
        const spread = bestAsk - bestBid;
        const midPrice = (bestAsk + bestBid) / 2;

        console.log('   ‚îú‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚î¥‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚î§');
        console.log(`   ‚îÇ  Spread: $${spread.toFixed(4)} | Mid: $${midPrice.toFixed(4)}  ‚îÇ`);
        console.log('   ‚îú‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚î¨‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚î§');

        const bids = message.bids.slice(0, 3);
        bids.forEach(bid => {
            console.log(`   ‚îÇ $${parseFloat(bid.price).toFixed(4)}  ‚îÇ  ${parseFloat(bid.size).toFixed(2)} shares  ‚îÇ`);
        });

        console.log('   ‚îÇ         BIDS (Buying)           ‚îÇ');
        console.log('   ‚îî‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îò');

        // Update position prices
        wallet.updatePositionPrices(tokenId, midPrice);

        // Execute a test order after first orderbook update (demo only)
        if (!hasExecutedTestOrder && orderbookData[yesTokenId] && orderbookData[noTokenId]) {
            hasExecutedTestOrder = true;

            console.log('\n\n‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ');
            console.log('üìã Step 7: Execute Test Virtual Orders');
            console.log('‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ');

            setTimeout(() => {
                const testSize = 10;
                const yesOrderbook = orderbookData[yesTokenId];

                console.log(`\nüéØ Testing: BUY ${testSize} YES tokens at MARKET price`);

                const order = wallet.simulateBuy(
                    yesTokenId,
                    'YES',
                    testSize,
                    fullMarket,
                    { bids: yesOrderbook.bids, asks: yesOrderbook.asks }
                );

                if (order.status === 'FILLED') {
                    console.log(wallet.getSummary());

                    // Test selling half the position after 5 seconds
                    setTimeout(() => {
                        console.log('\nüéØ Testing: SELL 5 YES tokens at MARKET price');

                        const sellOrder = wallet.simulateSell(
                            yesTokenId,
                            'YES',
                            5,
                            fullMarket,
                            { bids: yesOrderbook.bids, asks: yesOrderbook.asks }
                        );

                        if (sellOrder.status === 'FILLED') {
                            console.log(wallet.getSummary());
                        }
                    }, 5000);
                }
            }, 1000);
        }
    });

    // Handle errors
    stream.on('error', (error) => {
        console.error('‚ùå Stream error:', error);
    });

    // Keep the process running
    console.log('\nüí° Press Ctrl+C to exit\n');

    process.on('SIGINT', () => {
        console.log('\n\nüëã Shutting down...');
        console.log(wallet.getSummary());
        stream.disconnect();
        process.exit(0);
    });
}

// Run the demo
main().catch(error => {
    console.error('‚ùå Fatal error:', error);
    process.exit(1);
});
